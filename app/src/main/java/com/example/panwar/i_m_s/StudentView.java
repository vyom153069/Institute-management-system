public class StudentView {
    private int Student_REGISTRATION;
    private String Student_name;
    private String Student_FATHER_NAME;
    private String Student_Address;
    private String Student_Phone;
    private String Student_Course;

    public int getStudent_REGISTRATION() {

        return Student_REGISTRATION;
    }

    public void setStudent_REGISTRATION(int student_REGISTRATION) {
        Student_REGISTRATION = student_REGISTRATION;
    }

    public String getStudent_name() {
        return Student_name;
    }

    public void setStudent_name(String student_name) {
        Student_name = student_name;
    }

    public String getStudent_FATHER_NAME() {
        return Student_FATHER_NAME;
    }

    public void setStudent_FATHER_NAME(String student_FATHER_NAME) {
        Student_FATHER_NAME = student_FATHER_NAME;
    }

    public String getStudent_Address() {
        return Student_Address;
    }

    public void setStudent_Address(String student_Address) {
        Student_Address = student_Address;
    }

    public String getStudent_Phone() {
        return Student_Phone;
    }

    public void setStudent_Phone(String student_Phone) {
        Student_Phone = student_Phone;
    }

    public String getStudent_Course() {
        return Student_Course;
    }

    public void setStudent_Course(String student_Course) {
        Student_Course = student_Course;
    }
}
